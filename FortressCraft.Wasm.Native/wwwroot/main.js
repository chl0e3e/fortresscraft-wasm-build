// FortressCraft.Wasm.Native boot — threads + OffscreenCanvas.
//
// With WasmEnableThreads, the runtime + FNA run on a worker ("deputy") thread. The single
// `<canvas class="canvas">` is TRANSFERRED to that worker as an OffscreenCanvas by the
// dotnet.native.js `transferredCanvasNames=[".canvas"]` sed (applied post-build by
// build-bundle.sh). So FNA3D creates its WebGL2 context on the SAME thread it draws from —
// fixing the deputy-thread "getParameter on undefined GLctx" crash that the SDL2 head hit.
import { dotnet } from './_framework/dotnet.js';

// ---- boot progress / error surfacing -------------------------------------------------------
// A wasm game boot that fails is otherwise a silent black screen. We mirror boot stages + buffered
// output onto the on-page #boot overlay (index.html) and surface any error there. Output is also
// buffered to window.__fcLogs and POSTed to /__log on completion/failure (-> browser-log.txt).
const bootEl  = document.getElementById('boot');
const stageEl = document.getElementById('boot-stage');
const logEl   = document.getElementById('boot-log');
let booting = true;   // while true, the overlay mirrors the log; cleared once the game is running

// Buffer all runtime output so tooling can read window.__fcLogs regardless of devtools state.
const logs = (globalThis.__fcLogs = []);
function buf(kind, m) {
    const line = kind + ' ' + m;
    logs.push(line);
    if (logs.length > 2000) logs.shift();
    // Mirror to the overlay only during boot — the game logs every frame in-world, and updating
    // the DOM each line would drag the main thread once it's running.
    if (booting && logEl) { logEl.textContent = logs.slice(-400).join('\n'); logEl.scrollTop = logEl.scrollHeight; }
}
function stage(s) { if (stageEl) stageEl.textContent = s; buf('[boot]', s); console.log('[boot]', s); }
function postLog() {
    try {
        const body = logs.join('\n');
        if (navigator.sendBeacon) navigator.sendBeacon('/__log', body);
        else fetch('/__log', { method: 'POST', body, keepalive: true }).catch(() => {});
    } catch { /* best effort */ }
}
function fail(where, e) {
    booting = true;                                   // keep the overlay up and mirroring
    if (bootEl) bootEl.classList.add('err');
    stage('Failed during: ' + where);
    buf('[fatal]', (e && (e.stack || e.message)) ? (e.stack || e.message) : String(e));
    console.error('[boot] FAILED', where, e);
    postLog();
}
function bootDone() { booting = false; if (bootEl) setTimeout(() => bootEl.classList.add('hide'), 600); }

globalThis.addEventListener('error', (e) => buf('[jserror]', e.message + ' @ ' + e.filename + ':' + e.lineno));
globalThis.addEventListener('unhandledrejection', (e) => buf('[jsreject]', String((e.reason && e.reason.stack) || e.reason)));

const canvas = document.querySelector('canvas.canvas');

try {
    stage('Loading .NET runtime (cores=' + (navigator.hardwareConcurrency || '?') + ', crossOriginIsolated=' + self.crossOriginIsolated + ')');

    const api = await dotnet
        .withConfig({ pthreadPoolInitialSize: 16 })
        .withModuleConfig({
            canvas,
            // Buffer-only for stdout: the game logs every frame in-world; mirroring each line to
            // devtools drags the main thread. Read window.__fcLogs (or the /__log POST) instead.
            print: (m) => buf('[out]', m),
            printErr: (m) => {
                if (typeof m === 'string' && m.includes('emscripten_set_main_loop_timing')) return;
                buf('[err]', m);
                console.error('[native]', m);
            },
        })
        .create();

    const Module = api.Module;
    if (Module) Module.canvas = canvas;

    // --- Content + saves via WASMFS (Emscripten.c mounts) ----------------------------------
    // WASMFS replaces the legacy MEMFS; lazy XHR files don't apply. Instead mount an HTTP-fetch
    // backend over the static server's /Content (assets fetched on first read) and an OPFS backend
    // for persistent /saves. mount_fetch/mount_opfs are KEEPALIVE C funcs in Emscripten.c.
    stage('Mounting content + saves');
    try {
        const baseHref = location.href.slice(0, location.href.lastIndexOf('/') + 1);
        const r1 = Module.ccall('mount_fetch', 'number', ['string', 'string'], [baseHref + 'Content', '/Content']);
        buf('[content]', 'mount_fetch(/Content) -> ' + r1);
        const r2 = Module.ccall('mount_opfs', 'number', ['string'], ['/saves']);
        buf('[saves]', 'mount_opfs(/saves) -> ' + r2);

        // The fetch backend can't enumerate the remote tree: pre-register every directory and file
        // node so opens resolve (reads still fetch lazily). The manifest is generated by the server
        // (in production: by the patcher from the player's install).
        stage('Registering content manifest');
        const manifest = await fetch('__manifest').then(r => r.json());
        for (const d of manifest.dirs) {
            Module.ccall('mount_fetch', 'number', ['string', 'string'], ['', '/Content/' + d]);
        }
        let registered = 0;
        for (const f of manifest.files) {
            if (Module.ccall('mount_fetch_file', 'number', ['string'], ['/Content/' + f]) === 0) registered++;
        }
        buf('[fs]', `content manifest registered: ${manifest.dirs.length} dirs, ${registered}/${manifest.files.length} files`);
    } catch (e) {
        // Non-fatal to the runtime (the game may still boot far enough to report its own error),
        // but record it prominently — missing content is a common black-screen cause.
        buf('[fs]', 'CONTENT MOUNT/MANIFEST FAILED: ' + (e && e.stack || e));
        console.error('[fs] content mount failed:', e);
    }

    // FNA drives the loop via emscripten_set_main_loop(simulate_infinite_loop=1), which throws the
    // Emscripten 'unwind' marker to keep the runtime alive while requestAnimationFrame ticks.
    stage('Starting game (Program.Main → Game1)');
    try {
        await api.runMain();
        // Returned without unwinding: the game exited during startup (a boot failure the runtime
        // swallowed). Keep the overlay up so the logged stages show where it stopped.
        stage('Game exited during startup — see log below');
        postLog();
    } catch (e) {
        const msg = (e && (e.message ?? e.toString?.())) ?? String(e);
        if (e === 'unwind' || msg === 'unwind' || msg.includes('unwind')) {
            stage('Running');
            buf('[main]', 'FNA main loop running (requestAnimationFrame on the worker)');
            bootDone();
        } else {
            fail('Program.Main / Game1', e);
        }
    }
} catch (e) {
    fail('.NET runtime load', e);
}
